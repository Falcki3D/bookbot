def get_num_words(text):
    words = text.split()
    return len(words)


def get_chars_dict(text):
    chars = {}
    for c in text:
        lowered = c.lower()
        if lowered in chars:
            chars[lowered] += 1
        else:
            chars[lowered] = 1
    return chars


def sort_chars(chars_dict):
    sorted_chars = dict(sorted(chars_dict.items(), key=lambda x: x[1], reverse=True))
    return sorted_chars